"""Summarizer agent.

Computes the merge-gate decision deterministically (so the gate is auditable and
never depends on a flaky model), then renders the human-facing PR comment in
Markdown. The LLM contributes only a one-line headline; the gate and the body are
pure functions of the findings.
"""

from __future__ import annotations

from app.config import get_settings
from app.llm import chat_json
from app.models import FixSuggestion, GraphState, TriagedIssue
from app.policy import DEFAULT_POLICY, GatePolicy

HEADLINE_SYSTEM = (
    "You write a single-sentence headline summarizing a code-quality gate result "
    "for a pull request. Be factual and concise. Respond as JSON: {\"headline\": \"...\"}."
)


def _decide_gate(
    triaged: list[TriagedIssue],
    security: list[TriagedIssue],
    policy: GatePolicy,
) -> tuple[str, str]:
    blocking = policy.blocking_severities
    blockers = [t for t in triaged if t.issue.severity in blocking]
    vulns = [t for t in security if t.issue.type == "VULNERABILITY"]
    hotspots = [t for t in security if t.issue.type == "SECURITY_HOTSPOT"]

    bits = []
    if blockers:
        bits.append(f"{len(blockers)} blocking issue(s) ({'/'.join(blocking)})")
    if policy.fail_on_vulnerability and vulns:
        bits.append(f"{len(vulns)} open vulnerability(ies)")
    if policy.fail_on_security_hotspot and hotspots:
        bits.append(f"{len(hotspots)} security hotspot(s)")

    if bits:
        return "FAIL", "Gate failed: " + " and ".join(bits) + "."
    return "PASS", "Gate passed: no blocking issues or open vulnerabilities."


def _render_markdown(
    project: str,
    gate: str,
    reason: str,
    headline: str,
    triaged: list[TriagedIssue],
    security: list[TriagedIssue],
    fixes: list[FixSuggestion],
) -> str:
    emoji = "✅" if gate == "PASS" else "❌"
    lines = [
        f"## {emoji} GateKeeper — {gate}",
        "",
        f"**{headline}**",
        "",
        f"_{reason}_",
        "",
        f"**Project:** `{project}` &nbsp;&nbsp; **Findings:** {len(triaged)} open "
        f"&nbsp;&nbsp; **Security:** {len(security)}",
        "",
    ]

    if triaged:
        lines += [
            "### Top findings",
            "",
            "| Priority | Severity | Type | Location | Why it matters |",
            "|---|---|---|---|---|",
        ]
        for t in triaged[:10]:
            loc = (
                f"`{t.issue.component}:{t.issue.line}`"
                if t.issue.line
                else f"`{t.issue.component}`"
            )
            lines.append(
                f"| {t.priority} | {t.issue.severity} | {t.issue.type} | {loc} | {t.rationale} |"
            )
        lines.append("")

    if fixes:
        lines += ["### Suggested fixes", ""]
        for f in fixes:
            lines += [
                f"<details><summary><code>{f.component}</code> — {f.summary}</summary>",
                "",
                "```diff",
                f.patch,
                "```",
                "</details>",
                "",
            ]

    lines += [
        "---",
        "<sub>Generated by GateKeeper — SonarQube findings triaged by a "
        "LangGraph multi-agent system.</sub>",
    ]
    return "\n".join(lines)


def summarizer_node(state: GraphState) -> GraphState:
    settings = get_settings()
    report = state["report"]
    policy: GatePolicy = state.get("policy", DEFAULT_POLICY)
    triaged: list[TriagedIssue] = state.get("triaged", [])
    security: list[TriagedIssue] = state.get("security_findings", [])
    fixes: list[FixSuggestion] = state.get("fix_suggestions", [])

    gate, reason = _decide_gate(triaged, security, policy)

    headline = reason
    if not settings.use_mock:
        try:
            user = (
                f"Gate: {gate}. {reason} "
                f"Project {report.project} has {len(triaged)} open findings."
            )
            data = chat_json(HEADLINE_SYSTEM, user, settings.model_summary)
            headline = data.get("headline") or reason
        except Exception:
            pass  # fall back to the reason string on any LLM error

    summary_markdown = _render_markdown(
        report.project, gate, reason, headline, triaged, security, fixes
    )
    return {"gate": gate, "reason": reason, "summary_markdown": summary_markdown}
